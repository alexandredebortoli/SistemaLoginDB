    public TableUsuarios() {
        
        initComponents();
        List<Usuario> usuarios = UsuarioController.listaUsuarios();
        DefaultTableModel dtmUsuarios = (DefaultTableModel) tableUsuarios.getModel();
        String status = "";
        for (int i = 0; i < usuarios.size(); i++) {
            if(usuarios.get(i).getStatus()==1) {
                status = "ativo";
            } else if(usuarios.get(i).getStatus()==2) {
                status = "inativo";
            }
            dtmUsuarios.addRow(new String[]{Integer.toString(usuarios.get(i).getId()), usuarios.get(i).getNome(), usuarios.get(i).getEmail(),
               usuarios.get(i).getDataNascimento(), usuarios.get(i).getCargo(), status, usuarios.get(i).getCpf()});
        }
        
    }

